    var typeId = typeData.id;
    var currentTypeId = typeData.index;
    //将原始数据保存起来
    var initNavRightItemList = [];
    if (!this.data.initNavRightItemList)
      initNavRightItemList = this.data.navRightItemList;
    else
      initNavRightItemList = this.data.initNavRightItemList;
    //根据当前类型获取想要类型明细
    var navRightItemList = [];
    for(var i=0;i<initNavRightItemList.length;i++){
      var data = initNavRightItemList[i];
      if(typeId==data.typeId)
        navRightItemList.push(initNavRightItemList[i]);
    }
    //重置页面数据
    this.setData({
      "typeId": typeId,
      "currentTypeId": currentTypeId,
      "navRightItemList": navRightItemList,
      "initNavRightItemList": initNavRightItemList
    });
    this.onLoad(this);
